import numpy as np
import math 


def sine_calculator(x):
    """
    The first function.
    
    Args:
        x (float): Some number.
    Returns:
        float: Returns :math:`y=sin(x)`
    
    """
    return math.sin(x)


def ten_times_greater(x):
    """
    The second function.
    
    Args:
        x (float): Some number.
    Returns:
        float: Returns :math:`y=x*10`
        
    """
    return x*10





